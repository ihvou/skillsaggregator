export function difficultyForOrder(index, total) {
  if (total <= 1) return 1;
  return Math.round((1 + (index * 4) / (total - 1)) * 100) / 100;
}

export function itemsForCategory(categorySlug) {
  const slugs = CURATED_SUBSKILL_ORDERS[categorySlug] ?? [];
  return slugs.map((slug, index) => ({
    slug,
    learning_order: index + 1,
    subskill_difficulty: difficultyForOrder(index, slugs.length),
  }));
}
